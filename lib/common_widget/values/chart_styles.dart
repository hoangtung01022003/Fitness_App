class ChartData {
  final ChartStyles chartStyles;

  ChartData(this.chartStyles);

  List<LineChartBarData> getLineBarsData() {
    return [
      LineChartBarData(
        showingIndicators: chartStyles.showingTooltipOnSpots,
        spots: chartStyles.allSpots,
        isCurved: false,
        barWidth: 3,
        belowBarData: BarAreaData(
          show: true,
          gradient: LinearGradient(colors: [
            TColor.primaryColor2.withOpacity(0.4),
            TColor.primaryColor1.withOpacity(0.1),
          ], begin: Alignment.topCenter, end: Alignment.bottomCenter),
        ),
        dotData: FlDotData(show: false),
        gradient: LinearGradient(
          colors: TColor.primaryG,
        ),
      ),
    ];
  }

  LineChartBarData getTooltipsOnBar() {
    return getLineBarsData()[0]; // Access the first item from the list
  }
}